; Reset Handler

Reset_Handler   PROC
                EXPORT  Reset_Handler             [WEAK]                                            
				
				; imposto il PSP (unprivileged)
				MOV		r0, #3
				MSR		CONTROL, r0
				LDR		sp, =Stack_Mem
				
				; chiamata alla __main (main.c)
				IMPORT __main
				LDR r0, =__main
				BX r0
				
				;nop
				
				
InfLoop         B      	InfLoop	; infinite loop per la cosa vista a lezione sui programmi c
                ENDP


